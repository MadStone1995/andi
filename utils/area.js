/**
 * 获取省对应的所有城市
 */
function getCitys(provinceIndex) {
  var citys = [];
  for (var i = 0; i < AreaJson[provinceIndex].city.length; i++) {
    citys.push(AreaJson[provinceIndex].city[i].name);
  }
  return citys;
}

